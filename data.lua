function data.search(keyword)
    keyword = keyword:lower()
    local results = {}
    for id, record in pairs(data.records) do
        local found = false
        if id:lower():find(keyword, 1, true) or record.title:lower():find(keyword, 1, true) then
            found = true
        end
        if not found and record.keywords then
            for _, kw in ipairs(record.keywords) do
                if kw:find(keyword, 1, true) then
                    found = true
                    break
                end
            end
        end
        if not found then
            for _, text in ipairs(record.content) do
                if text:lower():find(keyword, 1, true) then
                    found = true
                    break
                end
            end
        end
        if found then
            table.insert(results, {id = id, record = record})
        end
    end
    table.sort(results, function(a, b) return a.id < b.id end)
    return results
end

